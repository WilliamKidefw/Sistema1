package com.william.sistema1.modelo;

public class Identificador {
	
	private int id;
	
	/**
	 * 
	 */
	public Identificador() {
		super();
		this.id=0;
	}

	/**
	 * @param id
	 */
	public Identificador(int id) {
		super();
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
